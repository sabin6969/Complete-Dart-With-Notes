// void is the return type of this function which means nothing
// main is a top level, special function from where the execution of program begins.
// main is a entry point for a program
// return type is optional.

display() {
  print("Display function");
}

void main() {
  print("Hello Dart");
  // calling a function
  display();
  // each and every statement in dart is terminated with semi-colon;
}
